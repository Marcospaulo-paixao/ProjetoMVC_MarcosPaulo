    private void jButtonDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletarActionPerformed
        // TODO add your handling code here:
        try {
            if (jComboBoxTipoDeConta.getSelectedIndex() == 0) {
                throw new Exception("Selecione o tipo de conta!");
            }
            String nome = jTextFieldNome.getText();
            String email = jTextFieldEmail.getText();
            String tipoDeConta = jComboBoxTipoDeConta.getSelectedItem().toString().split(" ")[1];
            this.cliente.setNome(nome);
            this.cliente.setEmail(email);

            Conta conta = null;
            conta = (cliente.getTipoDeConta().equals(TipoDeConta.COMUM))
                    ? ccControll.findByClienteId(this.cliente.getId())
                    : ceControll.findByClienteId(this.cliente.getId());

            if ((tipoDeConta.toUpperCase().equals("COMUM"))) {
                if (conta.getSaldo() != 0) {
                    throw new Exception("Não é possível deletar este cliente,\no saldo em conta é maior que 0.");
                }
                ccControll.delete(conta.getNumero());
            } else {
                if (conta.getSaldo() != 0) {
                    throw new Exception("Não é possível deletar este cliente,\no saldo em conta é maior que 0.");
                }
                ceControll.delete(conta.getNumero());
            }

            cliente.setNumeroConta(conta.getNumero());
            cliente.setTipoDeConta(conta.getTipoDeConta());

            cliControll.delete(cliente.getId());
            TableModelService.getInstance().update();

            this.dispose();

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        }
    }//GEN-LAST:event_jButtonDeletarActionPerformed

