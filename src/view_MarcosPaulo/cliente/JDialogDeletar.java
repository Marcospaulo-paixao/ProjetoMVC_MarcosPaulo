    public JDialogDeletar(java.awt.Frame parent, boolean modal, Cliente cliente) {
        super(parent, modal);
        initComponents();

        ImageIcon icone = new ImageIcon("assets/icons/deletar.png");
        setIconImage(icone.getImage());

        try {
            this.cliControll = new ClienteController("db/cliente.txt");
            this.ccControll = new ContaComumController("db/contaComum.txt");
            this.ceControll = new ContaEspecialController("db/contaEspecial.txt");

            this.cliente = cliente;

            jTextFieldNome.setText(cliente.getNome());
            jTextFieldEmail.setText(cliente.getEmail());
            String tipoDeConta = String.format("" + cliente.getTipoDeConta()).toLowerCase();
            String aux = Character.toUpperCase(tipoDeConta.charAt(0)) + tipoDeConta.substring(1);
            jComboBoxTipoDeConta.setSelectedItem("Conta " + aux);
            String saida = (cliente.getTipoDeConta().equals(TipoDeConta.ESPECIAL))
                    ? "" + this.ceControll.findByClienteId(cliente.getId()).getLimite() : "";

            jTextFieldLimite.setText(saida);

            jTextFieldNome.setEnabled(false);
            jTextFieldEmail.setEnabled(false);
            jComboBoxTipoDeConta.setEnabled(false);
            jTextFieldLimite.setEnabled(false);

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, erro);
        }
    }

