    private void jButtonTranferirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTranferirActionPerformed
        // TODO add your handling code here:
        try {
            Double valor = Double.parseDouble(jTextFieldValor.getText());
            Integer numero = Integer.parseInt(jTextFieldNumeroDaConta.getText());
            
            Conta conta1 = (ccControll.findByNumero(numero)!=null)?
                    ccControll.findByNumero(numero):
                    ceControll.findByNumero(numero);
            if (conta1 == null) throw new Exception("Conta não encontrado!");
            
            conta.transferir(conta1, valor);
            
            if(conta.getTipoDeConta() == TipoDeConta.COMUM)ccControll.update((ContaComum) conta);
            else ceControll.update((ContaEspecial) conta);
            
            if(conta1.getTipoDeConta() == TipoDeConta.COMUM)ccControll.update((ContaComum) conta1);
            else ceControll.update((ContaEspecial) conta1);
            
            JLabelSaldoService.getInstance().updateLabelSaldo();
            dispose();
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        }
    }//GEN-LAST:event_jButtonTranferirActionPerformed

