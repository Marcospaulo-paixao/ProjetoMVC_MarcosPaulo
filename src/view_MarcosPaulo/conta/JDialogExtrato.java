    public static String formatString( Cliente cliente,Conta conta){
        String extrato = "---------------------------------------------------------------------------------------------------------\n";
        extrato += "NOME:......................" + cliente.getNome().toUpperCase()+"\n";
        extrato += "EMAIL:......................" + cliente.getEmail()+"\n";
        extrato += "---------------------------------------------------------------------------------------------------------\n";        
        extrato += "CONTA "+((conta.getTipoDeConta() == TipoDeConta.COMUM)?conta.getTipoDeConta()+"":conta.getTipoDeConta())+":......"+conta.getNumero()+"\n";                        
        extrato += "SALDO ATUAL:..........." + formatMoney(conta.getSaldo())+"\n";
        extrato += (conta.getTipoDeConta() == TipoDeConta.COMUM)? "" :"LIMITE:......................"+ formatMoney(((ContaEspecial)conta).getLimite())+"\n";
        extrato += "---------------------------------------------------------------------------------------------------------\n";
        extrato += "" + conta.getExtrato().getTransacoes();
        extrato += "---------------------------------------------------------------------------------------------------------\n";
        return extrato;
    }
    public static String formatMoney(double valorBigDecimal) {
        return new DecimalFormat("'R$ ' #,###,##0.00").format(valorBigDecimal);
    }
